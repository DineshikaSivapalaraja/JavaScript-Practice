//print 1 to 10
for(var n=1;n<=10;n++){
    console.log(n)
}

//print 1 to 10 (only 1,3,5,7,9)
for(var o=1; o<=10; o=o+2){
    console.log(o)
}

//print 1 to 10 in reverse order
for(i=10;i>=1 && i<=10;i--){
    console.log(i)
}

//print even numbers between 1 to 10
for(n=2;n<=10;n+=2){
    console.log(n)
}

//print 2tables
for(n=1;n<=10;n++){
    console.log(n+"*2="+(n*2))
}